    <br /><h3>Paris est prêt à accueillir le Monde!</h3><p>

Sur le plan sportif, la France accueille notamment les championnats du monde d’aviron et les championnats d’Europe de Basketball en 2015, 7 compétitions internationales dont l’Euro de football en 2016, les championnats du monde de handball, de hockey-sur-glace et de lutte en 2017 ou encore la Ryder Cup en 2018, qui témoignent de la très grande attractivité du pays pour l’organisation de grandes compétitions sportives internationales. L’accueil des Jeux olympiques et paralympiques constituerait à cet égard à la fois une continuité et une opportunité exceptionnelles.

Les grands événements sportifs ne sont pas par nature bons ou mauvais pour un pays. Ils sont ce que les organisateurs décident d’en faire et reflètent les valeurs que le pays met dans la préparation de la candidature.

Trois exigences sont essentielles pour le projet olympique et paralympique : 

    <strong>l’éthique et la transparence </strong>;
    un modèle économique nouveau ;
    les jeux qui relèvent le défi du développement durable.  

Sur le plan de l’éthique, le Comité international olympique (CIO) a pris des engagements clairs qui permettent à Paris d’avoir une candidature conforme à ses valeurs fondamentales. Dans le cadre de son Agenda 2020, le CIO s’est en effet engagé à favoriser des candidatures respectueuses à la fois des citoyens et de l’environnement. La sobriété budgétaire et environnementale est l’une des clefs de cet engagement. 
